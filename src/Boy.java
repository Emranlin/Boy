public class Boy {
     private String name;
     private int age;
     private boolean isStudent;

     public void setName(String name) {
          this.name = name;
     }

     public String getName() {
          return name;
     }

     public void setAge(int age) {
          this.age = age;
     }

     public int getAge() {
          return age;
     }

     public void setStudent(boolean student) {
          isStudent = student;
     }

     public boolean isStudent() {
          return isStudent;

     }
}
